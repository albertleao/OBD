		Bourbon Tasting – May 2

		<p>
			Reserve your spot now for our next Bourbon Tasting, from 7 p.m. to 9 p.m. on May 2 at our Milton location. During this enjoyable event, we will feature Dickel Rye and Dickel Barrel Select as well as Bulleit Rye and Bulleit 10 Year.
		</p>

		<p>
			The cost is $25 per person and includes light appetizers.
		</p>

		<p>
			<b>George Dickel Rye:</b> Is 95 percent rye and 5 percent malted barley that is distilled and then aged in new charred oak barrels for at least five years. It carries aromas of toasty oak, spicy rye, citrus, pineapple, caramel, cinnamon, tangy cherry and menthol. The palate shows the aromas in a mild evolution of flavors that is initially sweet, with hints of fruit and builds to sapid oak nuance with vanilla, spice and caramel leading all the way through. The finish gently dawdles while quietly revealing roasted nuts, fennel, sassafras and camphor.
		</p>

		<p>
			<b>George Dickel Barrel Select:</b> Master Distiller John Lunn selects approximately 10 barrels of his best, most mellow whisky to produce an exceptionally fine small-batch Tennessee whisky. Aged 10–12 years, this 86-proof gem provides a consistently smooth taste, perfect balance, rich amber color and a hint of vanilla. Aided by charcoal mellowing and the slow aging process, these distinct characteristics create a super-premium whisky in a class of its own.
		</p>

		<p>
			<b>Bulleit Rye:</b> Aged in white oak barrels that are charred to the maximum before being filled, this whiskey is made with 95 percent rye, 5 percent malted barley and is a copper color with slight green patina. It’s brisk, vibrant, and loaded with spice (crisp mint, warming cinnamon, cocoa, hint of anise). Fruit -- ripe nectarine, apricot, golden raisin -- drizzled with maple syrup. Dry oak rounds out the finish. Bold enough for cocktails. Mature enough to drink neat (or on the rocks).
		</p>

		<p>
			<b>Bulleit 10 Year:</b> Choice Bulleit Bourbon barrels to age in charred American white oak barrels, resulting in a richer more complex bourbon experience. It’s russet in color, rich with oaky aromas and hints of vanilla and dried fruit with a long smoky finish.
		</p>

		<p>
			Seating is limited so call us at 678-624-1090 to make your reservation.
		</p>
